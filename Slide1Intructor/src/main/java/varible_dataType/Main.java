package varible_dataType;

public class Main {
    public static void main(String[] args) {
        int a = 10;
        float b = 7.5f;
        double d = 20.5;
        boolean i = true;
        char samsung = 's';
        String s = "Hue";

        System.out.println("Số nguyên int a: "+a);
        System.out.println("Số thực float b: "+b);
        System.out.println("Số thưc double d: "+d);
        System.out.println("Kiểu logic: "+i);
        System.out.println("Kiểu kí tự: "+samsung);
        System.out.println("Kiểu chuỗi: "+s);
    }
}
